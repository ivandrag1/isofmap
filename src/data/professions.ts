function cloneTasks(archetype: keyof typeof archetypes, professionId: string): Task[] {
  return archetypes[archetype].tasks.map((item, idx) => ({
    id: `${professionId}-task-${idx + 1}`,
    professionId,
    name: item[0],
    importance: item[1],
    automationPotential: item[2],
    augmentationPotential: item[3],
    humanJudgment: item[4],
    socialNeed: item[5],
    physicalContext: item[6],
    tags: item[7] as string[],
  }));
}

function cloneSkills(archetype: keyof typeof archetypes): ProfessionSkill[] {
  return futureSkillCatalog[archetype].map((s) => ({ ...s }));
}

function fit(archetype: keyof typeof archetypes): ProfessionFitProfile {
  return { ...archetypes[archetype].fit };
}

